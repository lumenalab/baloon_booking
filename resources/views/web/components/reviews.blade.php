<script>
document.addEventListener('DOMContentLoaded', function () {
    const tabs = document.querySelectorAll('.review-tab-button');
    const contentContainer = document.querySelector('.review-content');
    const panels = document.querySelectorAll('.review-panel');
    
    const setContainerHeight = (targetPanel) => {
        if (targetPanel) {
            const targetHeight = targetPanel.scrollHeight;
            contentContainer.style.minHeight = `${targetHeight}px`;
        }
    };

    const initialActivePanel = document.querySelector('.review-panel.active');
    setContainerHeight(initialActivePanel);

    tabs.forEach(tab => {
        tab.addEventListener('click', function () {
            const target = document.querySelector(this.dataset.target);
            tabs.forEach(t => t.classList.remove('active'));
            panels.forEach(p => p.classList.remove('active'));
            this.classList.add('active');
            target.classList.add('active');
            setContainerHeight(target);
        });
    });
});
</script>